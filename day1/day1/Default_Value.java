public class Default_Value {
	byte a;
	short b;
	int c;
	long d;
	float e;
	double f;
	char g;
	boolean h;
	public static void main(String args[]) {
		Default_Value d=new Default_Value();
		
		System.out.println("Byte values :"+d.a);
		System.out.println("short values :"+d.b);
		System.out.println("int values :"+d.c);
		System.out.println("Long values :"+d.d);
		System.out.println("Float values :"+d.e);
		System.out.println("double values :"+d.f);
		System.out.println("char values :"+d.g);
		System.out.println("boolean values :"+d.h);
	}

}
